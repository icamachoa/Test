DECLARE @IDEMPRESA INT 
DECLARE @EMPRESA VARCHAR(128) 
DECLARE @NOMBRE VARCHAR(128)
DECLARE @APELLIDO VARCHAR (128)  
DECLARE @CORREO VARCHAR(128) 
DECLARE @TELEFONO VARCHAR(128) 
DECLARE @CALLE VARCHAR(128) 
DECLARE @COLONIA VARCHAR(128) 
DECLARE @CIUDAD VARCHAR(128) 
DECLARE @CP VARCHAR(128) 
DECLARE @IDPAIS VARCHAR(20) 
DECLARE @IDESTADO VARCHAR(20) 
DECLARE @IDMUNICIPIO INT 
DECLARE @LATITUD VARCHAR(64)
DECLARE @LONGITUD  VARCHAR(64)
DECLARE @URL  VARCHAR(1024)


SET @IDEMPRESA=<#SESSION.IDEMPRESA/> 
SET @EMPRESA  =DBO.PREPARACADENA(ISNULL(:EMPRESA, '')) 
SET @NOMBRE=DBO.PREPARACADENA(ISNULL(:NOMBRE, ''))  
SET @APELLIDO=DBO.PREPARACADENA(ISNULL(:APELLIDO, '')) 
SET @CORREO=DBO.PREPARACADENA(ISNULL(:CORREO, ''))
