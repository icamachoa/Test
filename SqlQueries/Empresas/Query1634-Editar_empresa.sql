SET @CALLE=DBO.PREPARACADENA(ISNULL(:CALLE, ''))
SET @COLONIA= DBO.PREPARACADENA(ISNULL(:COLONIA, '')) 
SET @CIUDAD=DBO.PREPARACADENA(ISNULL(:CIUDAD, '') ) 
SET @CP=ISNULL(:CP, '')  
SET @IDPAIS=ISNULL(:SELECTPAIS, '')  
SET @IDESTADO=ISNULL(:SELECTESTADO, '')  
SET @IDMUNICIPIO= ISNULL(:SELECTMUNICIPIO, '')
SET @LATITUD=ISNULL(:LATITUD, '')
SET @LONGITUD=ISNULL(:LONGITUD, '')
SET @URL=ISNULL(:URL, '')
  

IF (@IDMUNICIPIO=0) SET @IDMUNICIPIO=''

UPDATE <#SESSION.DB/>.DBO.EMPRESAS_CONTACTO

SET 

EMPRESA=@EMPRESA,
URL =@URL,
CONTACTO_NOMBRE=@NOMBRE,
CONTACTO_APELLIDOS=@APELLIDO,
CORREO=@CORREO, 
TELEFONOS=@TELEFONO,
CALLE=@CALLE,
COLONIA=@COLONIA,
CIUDAD=@CIUDAD,
CP=@CP,
IDPAIS=@IDPAIS ,
IDESTADO=@IDESTADO,
IDMUNICIPIO=@IDMUNICIPIO,
LATITUD=@LATITUD,
LONGITUD=@LONGITUD

WHERE IDEMPRESA=@IDEMPRESA


